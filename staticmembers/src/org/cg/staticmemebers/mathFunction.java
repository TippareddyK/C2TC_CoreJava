package org.cg.staticmemebers;

public class mathFunction {

	public static void main(String[] args) {
		System.out.println("prints power 3*3 : "+Math.pow(3,2));
		System.out.println("prints square root 65 : "+Math.sqrt(65));
		System.out.println("prints random values>0and<1 "+Math.random());
		System.out.println("prints nearest value : "+Math.ceil(12.4));
		System.out.println("prints lesser close value : "+Math.floor(12.6));
		System.out.println("prints round figure : "+Math.round(12.4));
	}

}
